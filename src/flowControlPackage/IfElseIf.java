package flowControlPackage;

public class IfElseIf {
    public static void main(String[] args) {
        int n = 10;
        if (n > 0) {
            System.out.println("Positive");
        } else if (n < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("My number is Zero");
        }
    }
}
